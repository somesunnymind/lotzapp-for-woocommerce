// Load translations. WP 6.7+ requires this on `init` (or later), not earlier.
if (!function_exists('lotzwoo_load_main_textdomain')) {
    function lotzwoo_load_main_textdomain(?string $locale = null): void
    {
        $domain = 'lotzapp-for-woocommerce';
        $locale = $locale !== null && $locale !== '' ? $locale : get_locale();

        load_plugin_textdomain(
            $domain,
            false,
            dirname(plugin_basename(LOTZWOO_PLUGIN_FILE)) . '/languages'
        );

        $candidates = [
            trailingslashit(WP_LANG_DIR) . 'plugins/' . $domain . '-' . $locale . '.mo',
            trailingslashit(LOTZWOO_PLUGIN_DIR) . 'languages/' . $domain . '-' . $locale . '.mo',
        ];

        foreach ($candidates as $mo) {
            if (!is_string($mo) || !file_exists($mo)) {
                continue;
            }

            unload_textdomain($domain);
            load_textdomain($domain, $mo);
            return;
        }
    }
}
add_action('init', static function () {
    lotzwoo_load_main_textdomain();
});

/**
 * Per-email content translations.
 *
 * Each email's user-entered replace-field text (intro / body / reset_*) is
 * looked up at render-time via __() against a per-email text domain
 * "lotzapp-email-<email_id>". The matching .mo file lives at
 * wp-content/languages/plugins/lotzapp-email-<email_id>-<locale>.mo and is
 * generated by the LotzApp Übersetzungen page (or edited manually in Loco).
 * Registering them here also lets Loco's listener associate the domains with
 * this plugin bundle, so Loco shows them in the bundle view.
 */
if (!function_exists('lotzwoo_load_email_textdomains')) {
    /**
     * Load per-email translation .mo files for a specific locale.
     * Used at runtime (front-end email render) so the correct locale's
     * translations are active when __() is called.
     */
    function lotzwoo_load_email_textdomains(?string $locale = null): void
    {
        $locale = $locale !== null && $locale !== '' ? $locale : determine_locale();
        $dir    = trailingslashit(WP_LANG_DIR) . 'plugins/';
        if (!is_dir($dir)) {
            return;
        }
        $files = glob($dir . 'lotzapp-email-*-' . $locale . '.mo');
        if (!is_array($files)) {
            return;
        }
        $suffix_len = strlen('-' . $locale . '.mo');
        foreach ($files as $mo) {
            $basename = basename($mo);
            $domain   = substr($basename, 0, -$suffix_len);
            if ($domain === '' || strpos($domain, 'lotzapp-email-') !== 0) {
                continue;
            }
            unload_textdomain($domain);
            load_textdomain($domain, $mo);
        }
    }
}
if (!function_exists('lotzwoo_register_email_textdomains_for_loco')) {
    /**
     * Register every per-email text domain we have ANY .mo file for, so
     * Loco's listener associates them with this plugin bundle and lists
     * each per-email domain in Loco's bundle view. Runs in admin only,
     * where wrong-locale loading is harmless (no emails render there).
     */
    function lotzwoo_register_email_textdomains_for_loco(): void
    {
        $dir = trailingslashit(WP_LANG_DIR) . 'plugins/';
        if (!is_dir($dir)) {
            return;
        }
        $files = glob($dir . 'lotzapp-email-*-*.mo');
        if (!is_array($files)) {
            return;
        }
        $seen = [];
        foreach ($files as $mo) {
            $basename = basename($mo);
            if (!preg_match('/^(lotzapp-email-.+)-([a-z]{2,3}(?:_[A-Za-z0-9]{2,8})?)\.mo$/', $basename, $m)) {
                continue;
            }
            $domain = $m[1];
            if (isset($seen[$domain])) {
                continue;
            }
            $seen[$domain] = true;
            if (function_exists('is_textdomain_loaded') && is_textdomain_loaded($domain)) {
                continue; // already loaded by the runtime hook for current locale
            }
            load_textdomain($domain, $mo);
        }
    }
}
