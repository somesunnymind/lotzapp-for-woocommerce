/**
 * Register each per-email text domain as its own project on this plugin's
 * Loco bundle. Loco's auto-config only picks up the plugin's TextDomain header
 * (lotzapp-for-woocommerce); without this, our extra domains aren't part of
 * the bundle and Loco's file editor shows "Sync is disabled because this file
 * doesn't relate to a known set of translations." Adding them as projects
 * also makes them appear in the bundle overview alongside the main UI domain.
 */
add_action('loco_bundle_configured', static function ($bundle) {
    if (!class_exists('\\Loco_package_Plugin')) {
        return;
    }
    if (!($bundle instanceof \Loco_package_Plugin)) {
        return;
    }
    if ($bundle->getHandle() !== plugin_basename(LOTZWOO_PLUGIN_FILE)) {
        return;
    }

    $dir = trailingslashit(WP_LANG_DIR) . 'plugins/';
    if (!is_dir($dir)) {
        return;
    }
    $files = glob($dir . 'lotzapp-email-*-*.mo');
    if (!is_array($files)) {
        return;
    }

    $seen = [];
    foreach ($files as $mo) {
        $basename = basename($mo);
        if (!preg_match('/^(lotzapp-email-.+)-([a-z]{2,3}(?:_[A-Za-z0-9]{2,8})?)\.mo$/', $basename, $m)) {
            continue;
        }
        $domain = $m[1];
        if (isset($seen[$domain])) {
            continue;
        }
        $seen[$domain] = true;
        if ($bundle->getProject($domain)) {
            continue;
        }
        $project = new \Loco_package_Project(
            $bundle,
            new \Loco_package_TextDomain($domain),
            $domain
        );
        $bundle->addProject($project);
    }
}, 20);

// Lightweight PSR-4-like autoloader for the Lotzwoo namespace.
spl_autoload_register(static function ($class) {
    if (strpos($class, 'Lotzwoo\\') !== 0) {
        return;
    }

    $relative = substr($class, strlen('Lotzwoo\\'));
    $base_dir = LOTZWOO_PLUGIN_DIR . 'includes' . DIRECTORY_SEPARATOR;

    $candidates = [];

    $ns_path = str_replace('\\', DIRECTORY_SEPARATOR, $relative) . '.php';
    $candidates[] = $base_dir . $ns_path;
    $candidates[] = $base_dir . strtolower($ns_path);
    $ns_dir = dirname($ns_path);
    $ns_file = basename($ns_path);
    if ($ns_dir !== '.' && $ns_file !== '') {
        $candidates[] = $base_dir . $ns_dir . DIRECTORY_SEPARATOR . strtolower($ns_file);
    }

    $parts = explode('\\', $relative);
    $parts = array_map(static function ($part) {
        $part = preg_replace('/([a-z0-9])([A-Z])/', '$1_$2', $part);
        $part = preg_replace('/([A-Z])([A-Z][a-z])/', '$1_$2', $part);
        return strtolower($part);
    }, $parts);
    $candidates[] = $base_dir . implode(DIRECTORY_SEPARATOR, $parts) . '.php';

    foreach ($candidates as $file) {
        if ($file && file_exists($file)) {
            require_once $file;
            return;
        }
    }
});

